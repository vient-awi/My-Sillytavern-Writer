for (const key of SCHEMA_CORE_KEYS) {
  if (key in coreState) {
    newCoreState[key] = coreState[key];
  }
}

stat['核心状态'] = newCoreState;
stat['基础属性'] = baseAttr;

console.log('[1/5] 核心状态拆分为 核心状态(' + Object.keys(newCoreState).length + '字段) + 基础属性(' + Object.keys(baseAttr).length + '字段)');


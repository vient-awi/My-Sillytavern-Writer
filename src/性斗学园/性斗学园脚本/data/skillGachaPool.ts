/**
 * 技能抽取池数据
 * 包含所有可抽取的技能，按稀有度分类
 * 只使用MVU中已有的效果类型：性斗力、忍耐力、魅力、幸运、闪避率、暴击率、束缚、资源变化和特殊状态
 * C级: 基础技能，低消耗低伤害，无buff
 * B级: 稀有技能，中等消耗伤害，少量buff
 * A级: 精良技能，较高消耗伤害，有buff/debuff
 * S级: 传说技能，高消耗高伤害，复杂buff/debuff，可能有束缚
 * SS级: 神话技能，极高消耗极高伤害，多重复杂buff/debuff，有束缚
 */

export interface GachaSkillData {
  id: string;
  name: string;
  description: string;
  effectDescription: string;
  icon: string;
  type: string;
  rarity: 'C' | 'B' | 'A' | 'S' | 'SS';
  staminaCost: number;
  cooldown: number;
  damageSource: string;
  coefficient: number;
  accuracy: number;
  critModifier: number;
  hitCount: number;
  buffs: Array<{
    type:
      | '性斗力'
      | '忍耐力'
      | '魅力'
      | '幸运'
      | '闪避率'
      | '暴击率'
      | '束缚'
      | '快感变化'
      | '持续快感'
      | '耐力变化'
      | '持续耐力'
      | '敏感'
      | '乏力'
      | '迷离'
      | '集中'
      | '反弹'
      | '吸取快感';
    value: number;
    isPercent: boolean;
    duration: number;
    isTargetEnemy: boolean; // true=作用于敌人, false=作用于自己
  }>;
}

